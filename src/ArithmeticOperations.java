
public class ArithmeticOperations {

    public static void main(String[] args) {
        // 変数の宣言・初期化
        int num1=20;//int型変数を宣言し、20を代入
        int num2=100;//int型変数を宣言し、100を代入
        
        //int型変数result1を宣言し、num1とnum2の加算結果を代入
        int result1=num1+num2;
        //加算結果を出力
        System.out.println("num1+num2="+result1);
        
        //int型変数result2を宣言し、num1とnum2の減算結果を代入
        int result2=num1-num2;
        //減算結果を出力
        System.out.println("num1-num2="+result2);
        
        //int型変数result3を宣言し、num1とnum2の乗算結果を代入
        int result3=num1*num2;
        //乗算結果を出力
        System.out.println("num1*num2="+result3);
        
        //int型変数result4を宣言し、num1とnum2の除算結果を代入
        int result4=num1/num2;
        //除算結果を出力
        System.out.println("num1/num2="+result4);
        
        //int型変数result5を宣言し、num1とnum2の剰余算結果を代入
        int result5=num1%num2;       
        //剰余算結果を出力
        System.out.println("num1%num2="+result5);
        
        // 変数の宣言・初期化
        int num3=100;//int型変数を宣言し、100を代入
        int num4=10;//int型変数を宣言し、10を代入
        
        //int型変数resultを宣言
        int result;
        
        //変数resultにnum3とnum4の加算結果を代入
        result=num3+num4;       
        //加算結果を出力
        System.out.println("num3+num4="+result);
        
        //変数resultにnum3とnum4の減算結果を代入
        result=num3-num4;
        //減算結果を出力
        System.out.println("num3-num4="+result);
        //変数resultにnum3とnum4の乗算結果を代入
        result=num3*num4;
        //乗算結果を出力
        System.out.println("num3*num4="+result);
        //変数resultにnum3とnum4の除算結果を代入
        result=num3/num4;
        //除算結果を出力
        System.out.println("num3/num4="+result);
        //変数resultにnum3とnum4の剰余算結果を代入
        result=num3%num4;
        //剰余算結果を出力
        System.out.println("num3%num4="+result);

    }

}
